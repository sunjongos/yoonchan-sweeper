    log.info("[VISION] нҷ”л©ҙ к°җм§Җ лӘЁл“ң мӢңмһ‘")
    last_scan = 0
    last_pick = time.time()
    last_jump = time.time()
    holding: Optional[str] = None
    sw, sh = pyautogui.size()
    cx0, cy0 = sw // 2, sh // 2

    while not stopped():
        now = time.time()
        if now - last_pick >= jit(CFG["interact_interval"]):
            pick(); last_pick = now
        ji = CFG["jump_interval"]
        if ji > 0 and now - last_jump >= jit(ji):
            jump(); last_jump = now

        if now - last_scan >= CFG["vision_scan_interval"]:
            last_scan = now
            result = _scan()
            if holding:
                keyboard.release(holding); holding = None

            if result:
                ix, iy, name, cnt = result
                dx, dy = ix - cx0, iy - cy0
                nd = ("d" if dx > 0 else "a") if abs(dx) > abs(dy) else ("s" if dy > 0 else "w")
                keyboard.press(nd); holding = nd
                with state._lock:
                    state.stats.detected += 1
                    state.stats.cycles += 1
                log.info("[VISION] %s к°җм§Җ %dpx вҶ’ %s мқҙлҸҷ", name, cnt, nd)
            else:
                # м•„мқҙн…ң м—Ҷмңјл©ҙ лһңлҚӨ лӢЁкё° мқҙлҸҷ
                d = random.choice(list("wasd"))
                move(d, jit(0.7))
                with state._lock: state.stats.cycles += 1

        time.sleep(0.04)

    if holding:
        try: keyboard.release(holding)
        except: pass

