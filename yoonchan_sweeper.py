def run_grid():
    log.info("[GRID] мӢңмһ‘ (%dx%d)", CFG["grid_cols"], CFG["grid_rows"])
    cols = CFG["grid_cols"]
    rows = CFG["grid_rows"]
    step = jit(CFG["step_time"])
    while not stopped():
        for r in range(rows):
            lr = "d" if r % 2 == 0 else "a"   # м§Җк·ёмһ¬к·ё
            for _ in range(cols):
                move(lr, step)
                if stopped(): return
            if r < rows - 1:
                move("s", step * 0.7)
                if stopped(): return
        # мӣҗм җ ліөк·Җ
        move("w", step * rows * 0.7)
        move("a" if cols % 2 == 0 else "d", step * cols * 0.5)
        with state._lock: state.stats.cycles += 1
        _log_progress("GRID")

