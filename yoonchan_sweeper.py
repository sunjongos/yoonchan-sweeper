def jit(base: float) -> float:
    """нғҖмқҙл°Қ м§Җн„° вҖ” лҙҮ к°җм§Җ мҡ°нҡҢ"""
    if not CFG["jitter"]:
        return base
    return max(0.03, base + random.uniform(-CFG["jitter_max"], CFG["jitter_max"]))

def release_all():
    for k in "wasd":
        try: keyboard.release(k)
        except: pass

def pick():
    """FнӮӨ н”Ҫм—…"""
    keyboard.press_and_release("f")
    with state._lock:
        state.stats.pickups += 1
        state.stats.pickup_history.append(time.time())

def jump():
    keyboard.press_and_release("space")

def move(direction: str, dur: float, interact: bool = True):
    """
    л°©н–ҘнӮӨлҘј durмҙҲ лҸҷм•Ҳ лҲ„лҘҙл©ҙм„ң
    - FнӮӨ interact_intervalл§ҲлӢӨ лҲ„лҰ„
    - jump_intervalл§ҲлӢӨ м җн”„
    """
    last_pick = time.time()
    last_jump = time.time()
    keyboard.press(direction)
    t0 = time.time()
    try:
        while time.time() - t0 < dur:
            if state.stop.is_set():
                break
            now = time.time()
            if interact and now - last_pick >= jit(CFG["interact_interval"]):
                pick()
                last_pick = now
            ji = CFG["jump_interval"]
            if ji > 0 and now - last_jump >= jit(ji):
                jump()
                last_jump = now
            time.sleep(0.04)
    finally:
        keyboard.release(direction)

def stopped() -> bool:
    return state.stop.is_set()

